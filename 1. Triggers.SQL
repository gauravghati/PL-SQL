-- INSERT
DELIMITER $$
CREATE TRIGGER TRIG1 AFTER INSERT
ON PROJECT
FOR EACH ROW
BEGIN
    INSERT INTO TRIG_LOGS VALUES(CONCAT('NEW PROJECT IS INSERTED', NEW.PNAME));
END$$
DELIMITER ; 

-- DELETE
DELIMITER $$
CREATE TRIGGER TRIG2 AFTER DELETE
ON PROJECT
FOR EACH ROW
BEGIN
    INSERT INTO TRIG_LOGS VALUES(CONCAT('A PROJECT IS DELETED ', OLD.PNAME));
END$$
DELIMITER ;

-- UPDATE
DELIMITER $$
CREATE TRIGGER TRIG3 AFTER UPDATE
ON PROJECT
FOR EACH ROW
BEGIN
    INSERT INTO TRIG_LOGS VALUES(CONCAT(OLD.PNAME, CONCAT(' PROJECT IS UPDATED TO ', NEW.PNAME)));
END$$
DELIMITER ;

/*
6 TYPES OF TRIGGERS - 
    - AFTER INSERT
    - BEFORE INSERT
    - AFTER UPDATE
    - BEFORE UPDATE
    - AFTER DELETE
    - BEFORE DELETE

=> INSERT -> NEW
=> DELETE -> OLD
=> UPDATE -> NEW OLD

TRIGGERS ARE LIKE EVENT DRIVEN PROGRAM - AUTOMATICALLY TRIGGERED WHEN A EVENT IS OCCUR
BASICS OF TRIGGERS - https://www.youtube.com/watch?v=A3PQ6WtH_J4
*/
