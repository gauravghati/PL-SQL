DELIMITER $$
CREATE PROCEDURE PROC_PRO()
BEGIN
    DECLARE P_PNO INT;
    DECLARE P_PNAME VARCHAR(30);
    
    -- FOR HANDLING NOT FOUND CONDITION
    DECLARE FINISHED INTEGER DEFAULT 0; 
    DECLARE C1 CURSOR FOR SELECT PNO, PNAME FROM PROJECT;
    DECLARE CONTINUE HANDLER FOR NOT FOUND SET FINISHED=1; 
    OPEN C1; 

    GET_PRO: LOOP
        FETCH C1 INTO P_PNO, P_PNAME;
        IF FINISHED=1 THEN 
            LEAVE GET_PRO;
        END IF; 
        SELECT CONCAT(P_PNO, P_PNAME); 
    END LOOP GET_PRO; 
    CLOSE C1; 
END $$
DELIMITER ;

